
# __new__() method
# El __new__() method es el método constructor de la clase.


# .__init__() method
# El .__init__() method es el método inicializador de la clase.

# .__del__() method
# El .__del__() method es el método destructor de la clase.

# .__str__() method
# El .__str__() method es el método string de la clase. Es decir que es una representación textual de la clase.

# .__cmp__() method
# El .__cmp__() method es el método de comparación de la clase.

# .__len__() method
# El .__len__() method es el método de longitud de la clase.

