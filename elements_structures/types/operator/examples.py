
# Operadores de Asignaci√≥n.
# Operador = equivale a: variable = valor
variable = 10

# Operador += equivale a: variable = variable + valor
variable += 50              # variable tiene un valor de:  60

# Operador -= equivale a: variable = variable - valor
variable -= 25             # variable tiene un valor de:   35

# Operador *= equivale a: variable = variable * valor
variable *= 2              # variable tiene un valor de:   70

# Operador **= equivale a: variable = variable ** valor
variable **= 2             # variable tiene un valor de:   4900

# Operador //= equivale a: variable = variable // valor
variable //= 2             # variable tiene un valor de:   2450

# Operador %= equivale a: variable = variable % valor
variable %= 4              # variable tiene un valor de:   2

# Operador /= equivale a: variable = variable / valor
variable /= 2             # variable tiene un valor de:  1.0


